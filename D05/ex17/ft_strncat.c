char	*ft_strncat(char *dest, char *src, int nb)
{
	int		i;
	int		j;

	j = 0;
	i = 0;
	while (dest[i] != '\0')
		i++;
	while (nb != 0)
	{
		dest[i] = src[j];
		i++;
		j++;
		nb--;
	}
	dest[i] = '\0';
	return (dest);
}
