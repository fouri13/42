int		ft_is_prime(int nb)
{
	int prime;
	int	i;

	i = nb - 1;
	if (nb < 2)
		return (0);
	while (i > 1)
	{
		prime = nb % i;
		i--;
		if (prime == 0)
			return (0);
	}
	return (1);
}
