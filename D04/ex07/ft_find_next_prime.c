int		ft_is_prime(int nb)
{
	int prime;
	int i;

	prime = 0;
	i = nb - 1;
	if (nb < 2)
		return (0);
	while (i > 1)
	{
		prime = nb % i;
		i--;
		if (prime == 0)
			nb++;
	}
	return (nb);
}
